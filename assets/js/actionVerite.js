function rand(tab) {
    return Math.floor(Math.random() * tab.length)
}

function focusable() {
    joueur1.setAttribute('disabled', '')
    joueur2.setAttribute('disabled', '')
    niveau.setAttribute('disabled', '')
    partie.setAttribute('disabled', '')
}

function defocusable() {
    joueur1.removeAttribute('disabled')
    joueur2.removeAttribute('disabled')
    niveau.removeAttribute('disabled')
    partie.removeAttribute('disabled')
    action.setAttribute('disabled', '')
    verite.setAttribute('disabled', '')
    random_partie.setAttribute('disabled', '')
    rejouer.setAttribute('disabled', '')
}

